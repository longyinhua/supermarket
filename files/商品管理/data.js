function addPanle(id, type) {
    switch (type) {
        case "add":
            var value = $('#u190_input').val()
            if (value != null && value != '') {
                top.db.transaction(function (tx) {
                    tx.executeSql("select * from GOODS where code = ?", [value.toString()], function (tx, result) {
                        if (result.rows.length > 0) {
                            resetpanle()
                            openpanle()
                            $("#u291").get(0).attributes['data-id'].value = result.rows[0].id
                            $("#u297_text").text(result.rows[0].code)
                            $("#u304_img").get(0).src = !!result.rows[0].img ? result.rows[0].img : top.defaultCard()
                            $("#u299_input").val(result.rows[0].name)
                            $("#u301_input").val(result.rows[0].price)
                            $("#u306_input").val(result.rows[0].num)
                            $("#u302_textarea").val(result.rows[0].description)
                            $("#u291").get(0).attributes['data-time'].value = result.rows[0].createtime
                        } else {
                            resetpanle()
                            openpanle()
                            $("#u297_text").text(value)
                        }

                    }, function () {
                        console.info("数据加载异常！")
                    })
                })
            } else {
                //alert('请先输入商品编码')
                func('请先输入商品编码')
                focus()
                break
            }
            resetpanle()
            openpanle()
            break;
        case "show":
            getpanle(id)
            openpanle()
            break;
        case "edit":
            getpanle(id)
            openpanle()
            break;
    }
}


